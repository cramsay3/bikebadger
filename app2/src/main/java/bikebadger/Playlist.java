public class Playlist{

    private String from;
    private long id;
    private String name;

    public String GetFrom() {
        return from;
    }

    public void SetFrom(String from) {
        this.from = from;
    }

    public long GetId() {
        return id;
    }

    public void SetId(long id) {
        this.id = id;
    }

    public String GetName() {
        return name;
    }

    public void SetName(String name) {
        this.name = name;
    }

}
